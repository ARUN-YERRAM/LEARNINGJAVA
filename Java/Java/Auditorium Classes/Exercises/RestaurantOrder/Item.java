public class Item{
  public String dishname;
  public float price;
  public boolean isVeg;
  public String cuisine;
  public Item(String dishname, float price, boolean isVeg, String cuisine){
    super();
    this.dishname = dishname;
    this.price = price;
    this.isVeg = isVeg;
    this.cuisine = cuisine;
  }
}
